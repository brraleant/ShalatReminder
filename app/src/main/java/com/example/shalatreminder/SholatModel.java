package com.example.shalatreminder;

public class SholatModel {
    String name, date, time;

    public SholatModel(){
        this.name = name;
        this.date = date;
        this.time = time;
    }

    public String getDate() {
        return date;
    }

    public String getName() {
        return name;
    }

    public String getTime() {
        return time;
    }
}
